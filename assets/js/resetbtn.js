function resetFilters() {
    applyFilter(0, 'contrast', '100%');
    applyFilter(0, 'brightness', '100%');
    applyFilter(0, 'sepia', '0%');
    applyFilter(0, 'invert', '0%');
    applyFilter(0, 'grayscale', '0%');
    applyFilter(0, 'blur', '0px');
    applyFilter(0, 'hue-rotate', '0deg');
    applyFilter(0, 'saturate', '100%');
}